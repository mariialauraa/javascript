let numeros = [1, 5, 10, 15, 20, 25]

// Método PUSH (insere elementos no FINAL do Array):
numeros.push(30, 35, 40)

console.log(numeros)

// Método POP (remove um elemento no FINAL do Array):
let nomes = ["Maria", "Joao", "Pedro"]

let elementoRemovido = nomes.pop()
console.log("O elemento removido foi: " + elementoRemovido)
console.log(nomes)