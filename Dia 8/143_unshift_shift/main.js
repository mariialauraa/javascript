let numeros = [5, 10, 15, 20, 25]

// Método UNSHIFT (insere elementos no INÍCIO do Array):
numeros.unshift(1, 2, 3, 4)

console.log(numeros)

// Método SHIFT (remove o PRIMEIRO elemento do Array):
let nomes = ["Maria", "Joao", "Pedro"]

let elementoRemovido = nomes.shift()
console.log("O elemento removido foi: " + elementoRemovido)
console.log(nomes)